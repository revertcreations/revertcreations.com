<script type="text/javascript">
    let currentSlide = 0;
    const slides = document.querySelectorAll('.sp-slide');
    const dotContainer = document.querySelector('.sp-dots');

    slides.forEach((_, i) => {
        const dot = document.createElement('div');
        dot.classList.add('sp-dot');
        if (i === 0) dot.classList.add('active');
        dot.addEventListener('click', (e) => {
            e.stopPropagation();
            goToSlide(i);
        });
        dotContainer.appendChild(dot);
    });

    const dots = document.querySelectorAll('.sp-dot');

    function showSlide(index) {
        slides.forEach(s => s.style.display = 'none');
        dots.forEach(d => d.classList.remove('active'));

        slides[index].style.display = 'flex';
        dots[index].classList.add('active');
    }

    function goToSlide(index) {
        currentSlide = index;
        showSlide(currentSlide);
    }

    slides.forEach(slide => {
        slide.addEventListener('click', () => {
            window.location.href = slide.getAttribute('data-link');
        });
    });

    let slideInterval = setInterval(() => {
        currentSlide = (currentSlide + 1) % slides.length;
        showSlide(currentSlide);
    }, 2500);

    const container = document.querySelector('.sp-carousel-container');
    container.addEventListener('mouseenter', () => clearInterval(slideInterval));
    container.addEventListener('mouseleave', () => {
        slideInterval = setInterval(() => {
            currentSlide = (currentSlide + 1) % slides.length;
            showSlide(currentSlide);
        }, 2500);
    });

    showSlide(0);
</script>

