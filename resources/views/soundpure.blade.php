        const dot = document.createElement('div');
        dot.classList.add('sp-dot');
        if (i === 0) dot.classList.add('active');
        dot.addEventListener('click', (e) => {
            e.stopPropagation();
            goToSlide(i);
        });
        dotContainer.appendChild(dot);
